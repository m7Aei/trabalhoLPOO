    private void bttnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnCadastrarActionPerformed
        // TODO add your handling code here:
        Funcionario funcionario = new Funcionario();
        FuncionarioDAO saveFuncionario = new FuncionarioDAO();
        
        funcionario.setNome(textNome.getText());
        funcionario.setCargo(textCargo.getText());
        funcionario.setSalario(Float.parseFloat(textSalario.getText()));
        
        saveFuncionario.Create(funcionario);
        
        textNome.setText("");
        textCargo.setText("");
        textSalario.setText("");
        
        readTabela();
    }//GEN-LAST:event_bttnCadastrarActionPerformed

    private void tabelaInformacoesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaInformacoesKeyReleased
        // TODO add your handling code here:
        if (tabelaInformacoes.getSelectedRow() != -1) {
            textNome.setText(tabelaInformacoes.getValueAt(tabelaInformacoes.getSelectedRow(), 1).toString());
            textCargo.setText(tabelaInformacoes.getValueAt(tabelaInformacoes.getSelectedRow(), 2).toString());
            textSalario.setText(tabelaInformacoes.getValueAt(tabelaInformacoes.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tabelaInformacoesKeyReleased

    private void bttnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeletarActionPerformed
        // TODO add your handling code here:
        if (tabelaInformacoes.getSelectedRow() != -1) {
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO deleteFuncionario = new FuncionarioDAO();
            
            funcionario.setId((int) tabelaInformacoes.getValueAt(tabelaInformacoes.getSelectedRow(), 0));
            
            deleteFuncionario.Delete(funcionario);
            
            textNome.setText("");
            textCargo.setText("");
            textSalario.setText("");
            
            readTabela();
        }
    }//GEN-LAST:event_bttnDeletarActionPerformed

    private void bttnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnAtualizarActionPerformed
        // TODO add your handling code here:
        
        if (tabelaInformacoes.getSelectedRow() != -1) {
            Funcionario funcionario = new Funcionario();
            FuncionarioDAO updateFuncionario = new FuncionarioDAO();
            
            funcionario.setNome(textNome.getText());
            funcionario.setCargo(textCargo.getText());
            funcionario.setSalario(Float.parseFloat(textSalario.getText()));
            funcionario.setId((int)tabelaInformacoes.getValueAt(tabelaInformacoes.getSelectedRow(), 0));
            
            updateFuncionario.Update(funcionario);
            
            textNome.setText("");
            textCargo.setText("");
            textSalario.setText("");
            
            readTabela();
        }
    }//GEN-LAST:event_bttnAtualizarActionPerformed

