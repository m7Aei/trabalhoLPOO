    public void readTabela() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaInformacoes.getModel();
        modeloTabela.setNumRows(0);
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        
        
        funcionarioDAO.listaFuncionario().forEach((f) -> {
            modeloTabela.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCargo(),
                f.getSalario(),
                f.getSalarioLiquido()
            });
        });
    }

    public void readTabelaForNome(String nome) {
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaInformacoes.getModel();
        modeloTabela.setNumRows(0);
        FuncionarioDAO buscarFuncionarioDAO = new FuncionarioDAO();
        
        for (Funcionario f : buscarFuncionarioDAO.listaFuncionario(nome)) {
            modeloTabela.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCargo(),
                f.getSalario(),
                f.getSalarioLiquido()
            });
        }
    }
